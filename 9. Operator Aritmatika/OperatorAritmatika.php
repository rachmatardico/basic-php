<?php

$a = 10;
$b = 10;

$result = $a + $b;
var_dump($result);

// Negatif
$resultNegative = -$result;
var_dump($resultNegative);

// Positif
$resultPositive = +$result;
var_dump($resultPositive);

// Perkalian
$result = $a * $b;
var_dump($result);

// Pembagian
$result = $a / $b;
var_dump($result);

// Sisa bagi
$x = 100;
$y = 30;
$hasil = $x % $y;
